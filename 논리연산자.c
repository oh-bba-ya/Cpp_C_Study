int main()
{
	int a = 3;
	int b = 5;
	
	printf("%d\n", (a >= 3) && (b < 6));
	printf("%d\n", (a != 3) && (a > 2));
	printf("%d\n", (b != 5) || (a == 1));
	printf("%d\n", (a !=! b) || (b == 2));

	return 0;
}

